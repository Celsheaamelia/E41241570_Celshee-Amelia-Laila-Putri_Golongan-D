package Minggu3Pertemuan1;

public class Tugas2 {
    public static void main(String[] args) {
        int i = 1;
        
        System.out.println("          Do_While          ");
        System.out.println("Bilangan kelipatan 2 (1-100)");
        System.out.println("============================");
        
        do {
            System.out.print(i + "   ");
            i *= 2; 
        } while (i <= 100);
        
        System.out.println();
    }
}
